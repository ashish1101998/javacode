public class StrLength {

	    public static void main(String[] args) {  
	        String str = "Infostretch";  
	        if(str.length()>0) {  
	            System.out.println("First string length is: "+str.length());  
	        }  
	        str = "Company";  
	        if(str.length()>0) {  
	            System.out.println("Second string length is: "+str.length());  
	        }  
	    }  
	}
